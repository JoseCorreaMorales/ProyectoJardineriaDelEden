                <h1>The flowerpot, an important choice</h1>
        
                    <p>By José Manuel Olivian in <a href="https://medium.com/@Plantadecor/la-maceta-una-elecci%C3%B3n-importante-bff6f0dd475f" target="_blank">Medium</a></p><br>


                    <p> With certain frequency, the most attention to our care or choices is taken by the plants, a quite logical question, but it is not the only thing we should focus on. One of the items that is more important than what is often borrowed is the flowerpot. Although it is often considered a plant beautifier or simply a container, we have to take into account the types and characteristics they have to select the one that best suits our species and the circumstances where it will be. </p>
                    
        
                    <div style="text-align: center;"><img src="https://miro.medium.com/max/1400/0*QgOSm2q5WIR7Gu0A" alt=""  class="responsive_img" height="400" width="600">
                        
