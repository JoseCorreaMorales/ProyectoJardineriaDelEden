                        <p> <b> To make the right decision </b>, several aspects must be taken into account: </p> <br>
        
                        <ul>
                            <li>
                               
                                <p> <b> Where are you going to place it? </b> It is not the same outside as inside the home or premises. In this sense, those that are located outdoors must have a good drainage system to prevent the roots from rotting if it rains a lot or there is an excess of watering. It will also have to withstand extreme hot and cold temperatures. </p>
                            </li>
        
        
                            <li>
                                <p> <b> What material to choose? </b> There are multiple options such as plastic, terracotta, wood, resin, metal, glass, fiberglass ... Each one has its properties, which we will explain in a future post, and depending on The use that is going to be given to it and the place where it is put, one material or another will be more convenient. </p>
                            </li>
        
                            <li>
                            <p> <b> Is it for a plant that requires a special pot? </b> In this case the pot will be determined by said plant, in this typology there would be for example some orchids, bonsai, fruit trees ... </p>
                            </li>
                        </ul>
        
        
        
        
